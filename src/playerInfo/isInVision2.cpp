#include "playerInfo.hh"

bool ef::PlayerInfo::isInVisionUnit(std::shared_ptr<Unit> unit)
{
  return visionMap[unit->getActualPos().x + unit->getActualPos().y * visionMap.getMapSize().x] == 2;
}
