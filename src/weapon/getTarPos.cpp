#include "weapon.hh"

ef::Pos ef::Weapon::getTarPos()
{
  Pos temp;

  temp.x = -1;
  temp.y = -1;
  if (target.get() == nullptr)
    return temp;
  temp = target->getPos();
  return temp;
}

