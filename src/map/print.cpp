#include "map.hh"
#include <iostream>

void ef::Map::print()
{
  std::string str;
  for (int i = 0; i < mapSize.x * mapSize.y; i += 1)
    {
      str += map[i] + '0';
      if (i % mapSize.x == mapSize.x - 1)
	{
	  std::cout << str << std::endl;
	  str.clear();
	}
    }
}

