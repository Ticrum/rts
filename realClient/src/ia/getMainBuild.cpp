#include "ia.hh"

void ef::Ia::getMainBuild()
{
  std::vector<std::shared_ptr<Building>> build = cli.playerInfo.getBuild();
  for (int i = 0; i < (int)build.size(); i += 1)
    if (build[i]->getConf() == "MainBuild")
      MainBuild.push_back(build[i]);
}

